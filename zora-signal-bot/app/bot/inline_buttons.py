"""
app/bot/inline_buttons.py
─────────────────────────────────────────────────────────────────────────────
Telegram inline button callbacks for interactive tool responses.

Provides callback handlers for:
  - Buy/Sell confirmation buttons (after trade preview)
  - Close position buttons
  - Wallet linking confirmation
  - Creator tracking acknowledgment
  - Preference adjustment buttons

Buttons are generated by tool responses and handled via callback queries.
"""

from __future__ import annotations

import json
